function theme(
  id: string,
  label: string,
  description: string,
  light: Partial<ThemeTokens>,
  dark: Partial<ThemeTokens>,
  extras: Partial<ThemeDefinition> = {},
): ThemeDefinition {
  return {
    id,
    label,
    description,
    light: { ...defaultLight, ...light },
    dark: { ...defaultDark, ...dark },
    ...extras,
  };
}

